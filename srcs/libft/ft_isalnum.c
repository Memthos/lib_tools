#include "libft.h"

t_status	ft_isalnum(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122)
		|| (c >= 48 && c <= 57))
		return (SUCCESS);
	return (FAILURE);
}
