#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	if (!s || !c)
		return (NULL);
	i = 0;
	while (s[i])
	{
		if (s[i] == (char) c)
			return ((char *) &s[i]);
		i++;
	}
	if (s[i] == '\0' && s[i] == (char) c)
		return ((char *) &s[i]);
	return (NULL);
}
